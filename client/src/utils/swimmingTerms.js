// Функция для получения подсказки по стилю
export const getStyleHint = (style) => {
  return swimmingTerms[style] || null;
};

// Функция для получения подсказки по техническому термину
export const getTechnicalHint = (term) => {
  return technicalTerms[term] || null;
};

// Функция для получения подсказки по пульсовой зоне
export const getPulseHint = (pulse) => {
  return pulseZones[pulse] || null;
};

// Функция для получения подсказки по режиму тренировки
export const getModeHint = (mode) => {
  return trainingModes[mode] || null;
};

// Функция для поиска подсказки по любому термину
export const findHint = (term) => {
  const normalizedTerm = term.toLowerCase().trim();
  
  // Проверяем точные совпадения
  if (swimmingTerms[normalizedTerm]) {
    return { type: 'style', data: swimmingTerms[normalizedTerm] };
  }
  
  if (technicalTerms[normalizedTerm]) {
    return { type: 'technical', data: technicalTerms[normalizedTerm] };
  }
  
  if (pulseZones[normalizedTerm]) {
    return { type: 'pulse', data: pulseZones[normalizedTerm] };
  }
  
  if (trainingModes[normalizedTerm]) {
    return { type: 'mode', data: trainingModes[normalizedTerm] };
  }
  
  // Проверяем частичные совпадения
  const allTerms = {
    ...swimmingTerms,
    ...technicalTerms,
    ...pulseZones,
    ...trainingModes
  };
  
  for (const [key, value] of Object.entries(allTerms)) {
    if (normalizedTerm.includes(key) || key.includes(normalizedTerm)) {
      // Определяем тип по ключу
      if (swimmingTerms[key]) {
        return { type: 'style', data: value };
      } else if (technicalTerms[key]) {
        return { type: 'technical', data: value };
      } else if (pulseZones[key]) {
        return { type: 'pulse', data: value };
      } else if (trainingModes[key]) {
        return { type: 'mode', data: value };
      }
    }
  }
  
  return null;
};
